// while loop

let i = 0;
while (i <= 10) {
  console.log(`Value of i: ${i}`);
  i = i + 2;
}

let myArr = ["Optimus Prime", "Bumble Bee", "Ironhide", "Mirage"];
let arr = 0;
while (arr < myArr.length) {
  console.log(myArr[arr]);
  arr++;
}

// do-while loop

let score = 1;
do {
  console.log(`Score is: ${score}`);
  score++;
} while (score <= 10);
